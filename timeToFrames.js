var x = ["03:36:56", "04:07:30", "05:23:27", "05:19:12", "05:08:09", "03:49:21", "03:48:51", "06:17:40", "05:51:06", "06:45:32"]
var y = ["03:37:16", "04:08:04", "05:33:39", "05:21:51", "05:07:25", "03:08:24", "03:47:54", "05:24:29", "05:52:43", "06:44:50"]

formatTime = function (str) {
  var arr = str.split(':').map(e => +e).reverse();
  var ret = 0;
  ret += arr[0];
  ret += arr[1] * 60;
  ret += arr[2] * 3600;
  return ret;
}

x = x.map(formatTime)
y = y.map(formatTime)